package com.zth;

public class User {
    private String uname;
    private String upwd;
    private String name;

    public String getUname() {
        return uname;
    }

    public void setUname(String uname) {
        this.uname = uname;
    }

    public String getPwd() {
        return upwd;
    }

    public void setPwd(String pwd) {
        this.upwd = pwd;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
